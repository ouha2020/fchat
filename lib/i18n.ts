export type TranslationKey = keyof typeof DICTIONARY.zh;

export function normalizeLanguage(value: string | null | undefined): Language | null {
  if (!value) return null;
  const lower = value.toLowerCase();
  if (lower.startsWith("zh")) return "zh";
  if (lower.startsWith("ja")) return "ja";
  if (lower.startsWith("en")) return "en";
  return null;
}

export function detectBrowserLanguage(): Language {
  if (typeof navigator === "undefined") return "zh";
  const candidates = [navigator.language, ...(navigator.languages ?? [])];
  for (const candidate of candidates) {
    const normalized = normalizeLanguage(candidate);
    if (normalized) return normalized;
  }
  return "zh";
}

export function getStoredLanguage(): Language {
  if (typeof window === "undefined") return "zh";
  const stored = normalizeLanguage(window.localStorage.getItem(STORAGE_KEY));
  return stored ?? detectBrowserLanguage();
}

export function saveLanguage(language: Language): void {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(STORAGE_KEY, language);
}

export function translate(
  language: Language,
  key: TranslationKey,
  vars?: Record<string, string | number>,
): string {
  let text: string = DICTIONARY[language][key] ?? DICTIONARY.zh[key] ?? key;
  if (!vars) return text;
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}
